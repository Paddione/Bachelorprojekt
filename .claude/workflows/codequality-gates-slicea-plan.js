phase('Draft')
const authorSummary = await agent(
  'You are writing an execution-ready implementation plan. ' +
  'Write the COMPLETE plan as a single markdown file at exactly: ' + PLAN + '\n\n' +
  WP_RULES + '\n\n' + SLICE_A_SCOPE + '\n\n' + MUST_PIN + '\n\n' + GROUNDING + '\n\n' +
  'Produce the full file now (it will be long — that is expected; completeness over brevity). ' +
  'Return ONLY a short summary: the plan path, the number of tasks, and the concrete values you pinned for code_roots/ignore_globs, the scripts/ split, the per-extension S1 limits, and the S4 decision.',
  { label: 'draft:slice-a-plan', phase: 'Draft' }
)

const FINDINGS_SCHEMA = {
  type: 'object', additionalProperties: false,
  required: ['lens', 'verdict', 'findings'],
  properties: {
    lens: { type: 'string' },
    verdict: { type: 'string', description: 'one-line overall judgement' },
    findings: {
      type: 'array',
      items: {
        type: 'object', additionalProperties: false,
        required: ['severity', 'location', 'problem', 'fix'],
        properties: {
          severity: { type: 'string', enum: ['blocker', 'major', 'minor'] },
          location: { type: 'string', description: 'Task number / section / file:line in the plan' },
          problem: { type: 'string' },
          fix: { type: 'string', description: 'concrete change to make' },
        },
      },
    },
  },
}

const LENSES = [
  { key: 'spec-faithful', focus:
    'LENS: Spec-faithfulness + must-list coverage. Read the spec (' + SPEC + ') and the drafted plan (' + PLAN + '). ' +
    'Does EVERY Slice A spec requirement map to a task? Is B2 honored (every gate JSON carries a numeric `metric`; check.mjs uses the union rule, not a pure key-set diff)? B3 (per-gate key derivation, esp. S2 canonical sorted-cycle key per graph)? B1 (scan-universe = git ls-files ∩ code_roots − ignore_globs, file-level)? Are the MUST-PIN decisions actually pinned with concrete values, not deferred? Is anything from Slice B leaking into this plan? Flag gaps.' },
  { key: 'executable', focus:
    'LENS: Executability against the REAL repo. Read the plan (' + PLAN + ') and verify against the actual tree in ' + WT + '. ' +
    'Do referenced files/paths/commands exist? Does emit-index actually use git ls-files (not os.walk)? Will the pinned code_roots/ignore_globs + subsystems.yaml make emit NOT throw over real HEAD (spot-check a few uncovered dirs the spec review found: assets/, art-library/, pentest-dashboard/, mcp-browser/, top-level task.sh, website/ non-src)? Does the CI step match the existing test-inventory drift pattern in ci.yml (offline-tests job, npm ci at root)? Is the madge add correct and per-graph? Does quality:baseline:freeze produce a baseline that makes quality:check exit 0 immediately after? Are the gate fixtures realistic? Flag anything that would not run as written.' },
  { key: 'tdd-noplaceholder', focus:
    'LENS: TDD granularity, no-placeholders, type/interface consistency. Read the plan (' + PLAN + '). ' +
    'Is each task a real TDD loop (failing test with FULL code → run-fail → minimal impl with FULL code → run-pass → commit)? Any forbidden placeholder ("TBD", "add error handling", "write tests for the above", "similar to Task N", prose-without-code)? Are the baseline.json schema, the gate JSON contract (key/path/metric/detail), and the load.mjs/validate.mjs APIs byte-identical everywhere they appear across tasks? Any function/schema/property name that drifts between tasks? Flag each.' },
  { key: 'ordering', focus:
    'LENS: Ordering + incremental greenness. Read the plan (' + PLAN + '). ' +
    'Does the task order build bottom-up (load → validate → emit → individual gates → check → freeze → Taskfile → CI), with no task depending on a later task’s output? Does each task leave the repo in a committable, green state? Is there an explicit "emit does not throw over real HEAD" test BEFORE baseline:freeze and BEFORE wiring CI? Is the bootstrap risk handled (CI ratchet step added only after a green freeze exists)? Flag ordering hazards.' },
]

phase('Review')
const reviews = await parallel(LENSES.map(l => () =>
  agent(l.focus, { label: 'review:' + l.key, phase: 'Review', schema: FINDINGS_SCHEMA })
    .catch(() => ({ lens: l.key, verdict: 'review errored', findings: [] }))
))
const allFindings = reviews.filter(Boolean).flatMap(r => (r.findings || []).map(f => ({ ...f, lens: r.lens })))
const actionable = allFindings.filter(f => f.severity === 'blocker' || f.severity === 'major')
log(actionable.length + ' blocker/major findings to apply (' + allFindings.length + ' total)')

phase('Revise')
let reviserSummary = 'no actionable findings — plan left as drafted'
if (actionable.length > 0) {
  reviserSummary = await agent(
    'Revise the implementation plan at ' + PLAN + ' to resolve the confirmed review findings below. ' +
    'Edit the file in place. Apply every blocker and major; apply minors where trivial. Preserve the writing-plans format (header, File Structure, bite-sized TDD tasks, no placeholders, type-consistent schemas). ' +
    'After editing, re-scan your own result for placeholders and schema-name drift and fix any. Do NOT git commit/push.\n\n' +
    'FINDINGS (JSON):\n' + JSON.stringify(actionable, null, 2) + '\n\n' +
    'Return a short summary of what you changed and confirm no placeholders remain.',
    { label: 'revise:slice-a-plan', phase: 'Revise' }
  )
}

return {
  plan_path: PLAN,
  author_summary: authorSummary,
  review_verdicts: reviews.filter(Boolean).map(r => ({ lens: r.lens, verdict: r.verdict, n: (r.findings || []).length })),
  actionable_findings: actionable,
  minor_findings: allFindings.filter(f => f.severity === 'minor'),
  reviser_summary: reviserSummary,
}
